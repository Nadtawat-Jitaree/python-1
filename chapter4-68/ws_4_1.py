# รับค่า String มา
s = input("Enter String : ")
# เช็คความยาวข้อมูลที่รับมา
s_len = len(s)

# if เช็ค ถ้าความยาวของ str ที่รับมามากว่า 10
if s_len > 10:
    # ให้แสดงความยาว str ที่รับมา
    print("Your String length : ", s_len)

# แสดงผลค่าที่รับมา
print("Your Sring Enter is ", s)